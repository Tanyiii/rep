# Создаем пустой стек (используем список)
stack = []

# Добавляем элементы в стек
stack.append(10)  # Добавляем 10 на вершину стека
stack.append(20)  # Добавляем 20
stack.append(30)  # Добавляем 30

# Выводим состояние стека
print("Стек после добавления элементов:", stack)

# Удаляем элементы из стека
top_element = stack.pop()  # Удаляем 30 (вершина стека)
print("Удаленный элемент:", top_element)
print("Стек после удаления:", stack)

# Удаляем еще один элемент
next_element = stack.pop()  # Удаляем 20
print("Удаленный элемент:", next_element)
print("Стек после удаления:", stack)
